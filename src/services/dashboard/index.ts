import Cookies from "js-cookie";

export const getAllCourses = async () => {
  try {
    const res = await fetch(`/api/admin/course`, {
      method: "GET",
      headers: {
        Authorization: `Bearer ${Cookies.get("token")}`,
      },
    });

    const data = await res.json();

    return data;
  } catch (e) {
    console.log(e);
  }
};

